//   WHOAMI.CPP - Use of virtual functions
//
/////////////////////////////////////////////////////////////
#include <iostream.h>
/////////////////////////////////////////////////////////////
class A
{
 public:
    A() {} ;
    virtual void WhoAmI()
    	{ cout << "I am an object of class A \n" ; }
} ;
/////////////////////////////////////////////////////////////
class B :public A
{
 public:
    B() : A() {} ;
    void WhoAmI()
    	{ cout << "I am an object of class B \n" ; }
} ;
/////////////////////////////////////////////////////////////
void main()
{
 B * ptrB = new B ;
 A * ptrA = ptrB ;

	ptrB->WhoAmI() ;
	ptrA->WhoAmI() ;
} ;
